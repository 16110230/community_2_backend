package com.lawencon.community.pojo.activity;

import java.time.LocalDate;

public class PojoActivityIncomeReport {
	public String no;
	public String dateIncome;
	public String income;
	public String tax;
	public String fixIncome;

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getDateIncome() {
		return dateIncome;
	}

	public void setDateIncome(String dateIncome) {
		this.dateIncome = dateIncome;
	}

	public String getIncome() {
		return income;
	}

	public void setIncome(String income) {
		this.income = income;
	}

	public String getTax() {
		return tax;
	}

	public void setTax(String tax) {
		this.tax = tax;
	}

	public String getFixIncome() {
		return fixIncome;
	}

	public void setFixIncome(String fixIncome) {
		this.fixIncome = fixIncome;
	}

}
